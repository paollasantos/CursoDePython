# Programação Orientada a Objetos

class Calculadora():

    def Soma(x,y): # Function
        """
        Recebe os valores x e y e faz a soma total deles
        """
        return x + y

    def Subtrair(x,y):
        return x - y

    def Dividir(x,y):
        return x / y

    def Multiplicar(x,y):
        return x * y